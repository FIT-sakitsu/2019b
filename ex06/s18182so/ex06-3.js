function ChangeTab1() {
  var tab1 = document.getElementById('tab1')
  var tab2 = document.getElementById('tab2')
  var tab3 = document.getElementById('tab3')

  tab1.style.display = 'block'
  tab2.style.display = 'none'
  tab3.style.display = 'none'
}

